    private void tb_EstudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_EstudiantesMouseClicked
        tbMostrar.setSelectedIndex(tbMostrar.indexOfComponent(jPanel1));
        int fila = tb_Estudiantes.getSelectedRow();

        String ID = String.valueOf(tb_Estudiantes.getValueAt(fila, 0));
        String IDPersona = String.valueOf(tb_Estudiantes.getValueAt(fila, 1));
        String Matricula = String.valueOf(tb_Estudiantes.getValueAt(fila, 3));
        String Usuario = String.valueOf(tb_Estudiantes.getValueAt(fila, 4));
        String Pass = String.valueOf(tb_Estudiantes.getValueAt(fila, 5));
        String Nie = String.valueOf(tb_Estudiantes.getValueAt(fila, 6));

        txtId.setText(ID);
        txtMatricula.setText(Matricula);
        txtUsuario.setText(Usuario);
        txtPss.setText(Pass);
        txtNie.setText(Nie);

        int seleccionadorvista = 0;
        for (var iterar : valueMember) {
            if (IDPersona.equals(iterar)) {
                cbPersonas.setSelectedIndex(seleccionadorvista);
            }
            seleccionadorvista += 1;
        }
    }//GEN-LAST:event_tb_EstudiantesMouseClicked

