    void MostrarTablaEstudiante() {
        String Titulos[] = {"ID", "IDPERSONA", "NOMBRE", "MATRICULA", "USUARIO", "CONTRASEÑA", "NIE"};
        DefaultTableModel ModeloTabla = new DefaultTableModel(null, Titulos);
        ClsEstudiante claseEstudiante = new ClsEstudiante();
        ArrayList<Estudiante> Estudiantes = claseEstudiante.MostrarPersona();
        String filas[] = new String[7];
        for (var IterarDatosEstudiante : Estudiantes) {
            filas[0] = String.valueOf(IterarDatosEstudiante.getId());
            filas[1] = String.valueOf(IterarDatosEstudiante.getIdPersona());
            filas[2] = String.valueOf(IterarDatosEstudiante.getNombre());
            filas[3] = String.valueOf(IterarDatosEstudiante.getMatricula());
            filas[4] = String.valueOf(IterarDatosEstudiante.getUsu());
            filas[5] = IterarDatosEstudiante.getPass();
            filas[6] = String.valueOf(IterarDatosEstudiante.getNIE());
            ModeloTabla.addRow(filas);
        }
        tb_Estudiantes.setModel(ModeloTabla);
    }

